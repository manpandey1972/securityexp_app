/// List item widget for displaying a skill category in admin views.
class SkillCategoryListItem extends StatelessWidget {
  final SkillCategory category;
  final int skillCount;

  const SkillCategoryListItem({
    super.key,
    required this.category,
    required this.skillCount,
  });

  @override
  Widget build(BuildContext context) {
    return Card(
      color: AppColors.surface,
      margin: const EdgeInsets.only(bottom: 8),
      child: ListTile(
        leading: _CategoryAvatar(category: category),
        title: Text(
          category.name,
          style: AppTypography.bodyRegular.copyWith(
            color: AppColors.textPrimary,
          ),
        ),
        subtitle: Text(
          '$skillCount ${skillCount == 1 ? 'skill' : 'skills'}',
          style: AppTypography.bodySmall.copyWith(
            color: AppColors.textSecondary,
          ),
        ),
        trailing: const _AutoGeneratedBadge(),
      ),
    );
  }
}

class _CategoryAvatar extends StatelessWidget {
  final SkillCategory category;

  const _CategoryAvatar({required this.category});

  @override
  Widget build(BuildContext context) {
    return CircleAvatar(
      backgroundColor: AppColors.primary.withValues(alpha: 0.1),
      child: Text(
        category.icon ?? 'üõ†Ô∏è',
        style: const TextStyle(fontSize: 20),
      ),
    );
  }
}

class _AutoGeneratedBadge extends StatelessWidget {
  const _AutoGeneratedBadge();

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 4),
      decoration: BoxDecoration(
        color: AppColors.primaryLight.withValues(alpha: 0.1),
        borderRadius: BorderRadius.circular(4),
      ),
      child: Text(
        'Auto-generated',
        style: AppTypography.bodySmall.copyWith(
          color: AppColors.primaryLight,
          fontSize: 10,
        ),
      ),
    );
  }
}

