/// Spacing and sizing constants for consistent layout in the Security Experts app.
class AppSpacing {
  AppSpacing._(); // Private constructor to prevent instantiation

  // ====================
  // Space Scale (4px base unit)
  // ====================
  /// 4pt spacing - smallest unit
  static const double spacing4 = 4.0;

  /// 8pt spacing - extra small
  static const double spacing8 = 8.0;

  /// 12pt spacing - small
  static const double spacing12 = 12.0;

  /// 16pt spacing - medium (most common)
  static const double spacing16 = 16.0;

  /// 20pt spacing - medium-large
  static const double spacing20 = 20.0;

  /// 24pt spacing - large
  static const double spacing24 = 24.0;

  /// 32pt spacing - extra large
  static const double spacing32 = 32.0;

  /// 40pt spacing - 2x large
  static const double spacing40 = 40.0;

  /// 48pt spacing - 3x large
  static const double spacing48 = 48.0;

  // ====================
  // Common Layout Padding
  // ====================
  /// Horizontal padding for screens (typically left/right margins)
  static const double screenHorizontalPadding = 16.0;

  /// Vertical padding for screens (typically top/bottom margins)
  static const double screenVerticalPadding = 16.0;

  /// Horizontal padding for cards
  static const double cardHorizontalPadding = 16.0;

  /// Vertical padding for cards
  static const double cardVerticalPadding = 12.0;

  // ====================
  // Common Layout Gaps
  // ====================
  /// Gap between elements in a column
  static const double columnGap = 16.0;

  /// Gap between elements in a row
  static const double rowGap = 12.0;

  /// Horizontal gap for symmetric spacing
  static const double horizontalGap = 16.0;

  /// Vertical gap for symmetric spacing
  static const double verticalGap = 8.0;
}
