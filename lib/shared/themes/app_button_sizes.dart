/// Button size constants for consistent sizing across the app.
/// Uses Material Design 3 sizing recommendations.
class AppButtonSizes {
  AppButtonSizes._(); // Private constructor to prevent instantiation

  // ====================
  // Button Heights
  // ====================
  /// Small button height (compact, icon buttons)
  static const double smallHeight = 32;

  /// Medium button height (standard, most common)
  static const double mediumHeight = 44;

  /// Large button height (prominent, primary actions)
  static const double largeHeight = 56;

  // ====================
  // Horizontal Padding
  // ====================
  /// Small button horizontal padding
  static const double smallPaddingX = 12;

  /// Medium button horizontal padding (standard)
  static const double mediumPaddingX = 16;

  /// Large button horizontal padding
  static const double largePaddingX = 24;

  // ====================
  // Icon Button Sizes
  // ====================
  /// Extra small icon button (tight spacing)
  static const double iconButtonXSmall = 28;

  /// Small icon button
  static const double iconButtonSmall = 36;

  /// Medium icon button (standard)
  static const double iconButtonMedium = 44;

  /// Large icon button
  static const double iconButtonLarge = 52;

  // ====================
  // Icon Sizes (for use within buttons)
  // ====================
  /// Small icon (16px - used in compact buttons/chips)
  static const double iconSmall = 16;

  /// Standard icon (20px - most common)
  static const double iconStandard = 20;

  /// Medium icon (24px - prominent actions)
  static const double iconMedium = 24;

  /// Large icon (28px - full-screen actions)
  static const double iconLarge = 28;

  /// Extra large icon (32px - hero actions)
  static const double iconXLarge = 32;
}
